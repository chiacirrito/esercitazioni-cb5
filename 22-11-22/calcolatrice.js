

function somma (n1, n2){
    return parseFloat(n1) + parseFloat(n2);
}

function sottrazione(n1, n2){
    return parseFloat(n1) - parseFloat(n2);
}

function moltiplicazione(n1, n2){
    return parseFloat(n1) * parseFloat(n2);
}

function divisione(n1, n2){
    return parseFloat(n1) / parseFloat(n2);
}

export{somma, sottrazione, moltiplicazione, divisione}